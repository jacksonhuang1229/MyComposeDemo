class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            val navController = rememberNavController()

            MyCompose2024Theme {
                // A surface container using the 'background' color from the theme
//                Surface(
//                    modifier = Modifier.fillMaxSize(),
//                    color = MaterialTheme.colorScheme.background
//                ) {
//                    Greeting("Android")
//                }
                NavHost(navController = navController, startDestination = HomeScreen.NAME) {

                    composable(HomeScreen.NAME) {
                        android.util.Log.d("testtttt" , " entry HomeScreen $this")
                        HomeScreen(navController)
                    }
                    composable(NewsScreen.NAME) {
                        android.util.Log.d("testtttt" , " entry NewsScreen $this")
                        NewsScreen(navController)
                    }
                }
            }
        }
    }
}

